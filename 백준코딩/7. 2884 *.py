# 파이썬에서 음수 나머지연산 %
#  파이썬은 나머지가 항상 나눗수(여기서는 24)의 부호를 따르고 양수가 되도록 조정합니다.
# 따라서 -2%24이면 -2 = 24*-1 + 22로서, 몫이 -1이고 나머지가 22가된다.
